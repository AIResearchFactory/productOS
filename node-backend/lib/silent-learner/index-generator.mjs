import fs from 'node:fs/promises';
import path from 'node:path';
import { getProjectById } from '../projects.mjs';
import { safeJoin } from '../paths.mjs';

const ARTIFACT_TYPE_LABELS = {
  roadmap: 'Roadmaps',
  product_vision: 'Product Visions',
  one_pager: 'One Pagers',
  prd: 'PRDs',
  initiative: 'Initiatives',
  competitive_research: 'Competitive Research',
  user_story: 'User Stories',
  insight: 'Insights',
  presentation: 'Presentations',
  pr_faq: 'PR FAQs',
};

function getSidecarPath(artifactPath) {
  if (artifactPath.endsWith('.md')) return artifactPath.replace(/\.md$/, '.json');
  const parsed = path.parse(artifactPath);
  return path.join(parsed.dir, `${parsed.name || parsed.base}.json`);
}

export const INDEX_FILE_NAME = 'index.md';
export const DEFAULT_INDEX_DEBOUNCE_MS = 30_000;

const pendingRegenerations = new Map();

function titleCase(value) {
  return String(value || '')
    .replace(/[-_]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()
    .replace(/\b\w/g, (letter) => letter.toUpperCase());
}

function markdownEscape(value) {
  return String(value || '')
    .replace(/\\/g, '\\\\')
    .replace(/\[/g, '\\[')
    .replace(/\]/g, '\\]')
    .replace(/\|/g, '\\|')
    .replace(/\n+/g, ' ')
    .trim();
}

function normalizeRelPath(relPath) {
  return String(relPath || '').replace(/\\/g, '/').replace(/^\/+/, '');
}

function formatMarkdownLink(relPath) {
  return normalizeRelPath(relPath).split('/').map(encodeURIComponent).join('/');
}

async function readJsonIfExists(filePath) {
  try {
    return JSON.parse(await fs.readFile(filePath, 'utf8'));
  } catch (err) {
    if (err?.code === 'ENOENT') return null;
    return null;
  }
}

async function readManifest(projectPath) {
  const manifestPath = path.join(projectPath, '.metadata', 'artifacts.json');
  const data = await readJsonIfExists(manifestPath);
  return Array.isArray(data) ? data : [];
}

function sidecarDescription(sidecar, artifact) {
  const candidates = [
    sidecar?.description,
    sidecar?.summary,
    sidecar?.abstract,
    sidecar?.metadata?.description,
    sidecar?.metadata?.summary,
    sidecar?.classification?.description,
    artifact?.description,
    artifact?.summary,
    artifact?.metadata?.description,
    artifact?.metadata?.summary,
  ];
  const description = candidates.find((value) => typeof value === 'string' && value.trim());
  return description ? description.trim().replace(/\s+/g, ' ') : 'No description yet.';
}

async function hydrateArtifact(projectPath, artifact) {
  const relPath = normalizeRelPath(artifact.path || artifact.id);
  const sidecarPath = await safeJoin(projectPath, getSidecarPath(relPath));
  const sidecar = await readJsonIfExists(sidecarPath) || {};
  return {
    ...artifact,
    path: relPath,
    title: artifact.title || sidecar.title || titleCase(path.parse(relPath).name),
    artifactType: artifact.artifactType || sidecar.artifactType || 'artifact',
    updated: artifact.updated || sidecar.updated || sidecar.updatedAt || artifact.created,
    created: artifact.created || sidecar.created || sidecar.createdAt,
    description: sidecarDescription(sidecar, artifact),
  };
}

function groupArtifacts(artifacts) {
  const groups = new Map();
  for (const artifact of artifacts) {
    const type = artifact.artifactType || 'artifact';
    const label = ARTIFACT_TYPE_LABELS[type] || titleCase(type);
    if (!groups.has(label)) groups.set(label, []);
    groups.get(label).push(artifact);
  }
  return [...groups.entries()]
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([label, items]) => [
      label,
      items.sort((a, b) => (a.title || '').localeCompare(b.title || '')),
    ]);
}

export async function generateIndexMarkdown(projectId, options = {}) {
  const project = await getProjectById(projectId);
  const manifest = options.artifacts || await readManifest(project.path);
  const artifacts = (await Promise.all(manifest
    .filter((artifact) => normalizeRelPath(artifact.path || artifact.id).toLowerCase().endsWith('.md'))
    .map((artifact) => hydrateArtifact(project.path, artifact))))
    .filter((artifact) => artifact.path !== INDEX_FILE_NAME && artifact.path !== 'log.md');

  const generatedAt = new Date().toISOString();
  const lines = [
    '# Project Index',
    '',
    '<!-- Auto-generated by Silent Learner. Content files remain user-owned; navigation is regenerated from artifact sidecars. -->',
    '',
    `Generated: ${generatedAt}`,
    '',
    `Artifacts: ${artifacts.length}`,
    '',
  ];

  if (artifacts.length === 0) {
    lines.push('No artifacts discovered yet.', '');
    return lines.join('\n');
  }

  for (const [label, items] of groupArtifacts(artifacts)) {
    lines.push(`## ${markdownEscape(label)}`, '');
    for (const artifact of items) {
      const link = formatMarkdownLink(artifact.path);
      const description = markdownEscape(artifact.description || 'No description yet.');
      const updated = artifact.updated ? ` — updated ${artifact.updated}` : '';
      lines.push(`- [${markdownEscape(artifact.title)}](${link}) — ${description}${updated}`);
    }
    lines.push('');
  }

  return lines.join('\n');
}

export async function regenerateProjectIndex(projectId, options = {}) {
  const project = await getProjectById(projectId);
  const markdown = await generateIndexMarkdown(projectId, options);
  const indexPath = await safeJoin(project.path, INDEX_FILE_NAME);
  const tempPath = `${indexPath}.tmp-${process.pid}-${Date.now()}`;
  await fs.writeFile(tempPath, markdown, 'utf8');
  await fs.rename(tempPath, indexPath);
  return { path: INDEX_FILE_NAME, content: markdown };
}

export function scheduleIndexRegeneration(projectId, options = {}) {
  const debounceMs = Number.isFinite(options.debounceMs) ? options.debounceMs : DEFAULT_INDEX_DEBOUNCE_MS;
  const existing = pendingRegenerations.get(projectId);
  if (existing) clearTimeout(existing);

  const timer = setTimeout(async () => {
    pendingRegenerations.delete(projectId);
    try {
      await regenerateProjectIndex(projectId);
    } catch (err) {
      console.error(`[SilentLearner] Failed to regenerate index.md for ${projectId}:`, err);
    }
  }, Math.max(0, debounceMs));
  if (typeof timer.unref === 'function') timer.unref();
  pendingRegenerations.set(projectId, timer);
}

export async function flushPendingIndexRegeneration(projectId) {
  const timer = pendingRegenerations.get(projectId);
  if (timer) {
    clearTimeout(timer);
    pendingRegenerations.delete(projectId);
  }
  return regenerateProjectIndex(projectId);
}

export async function getProjectIndex(projectId, options = {}) {
  const project = await getProjectById(projectId);
  const indexPath = await safeJoin(project.path, INDEX_FILE_NAME);
  try {
    return { path: INDEX_FILE_NAME, content: await fs.readFile(indexPath, 'utf8') };
  } catch (err) {
    if (err?.code !== 'ENOENT' || options.generateIfMissing === false) throw err;
    return regenerateProjectIndex(projectId);
  }
}
